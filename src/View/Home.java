    public void updateImage(byte[] imageData, String indexNum) {
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            Connection connection = DriverManager.getConnection(DB_URL, DB_USER, DB_PASSWORD);

            String sql = "UPDATE image SET img = ? WHERE std_id = ?";
            PreparedStatement statement = connection.prepareStatement(sql);
            statement.setBytes(1, imageData);
            statement.setString(2, indexNum);

            int rowsUpdated = statement.executeUpdate();

            if (rowsUpdated > 0) {
                System.out.println("Image updated successfully.");
            } else {
                System.out.println("No image found for index number: " + indexNum);
            }

            statement.close();
            connection.close();
        } catch (SQLException | ClassNotFoundException e) {
            e.printStackTrace();
            System.out.println("Failed to update image.");
        }
    }
    
    
    
    private byte[] getImageDataFromDatabase() {
        try {
            //  connection to database
            Connection connection = DriverManager.getConnection("jdbc:mysql://localhost:3307/studentlms", "root", "");

            
            String sql = "SELECT img FROM image where std_id='"+indexSearchTF.getText()+"'";
            PreparedStatement statement = connection.prepareStatement(sql);

         
            ResultSet resultSet = statement.executeQuery();

       
            if (resultSet.next()) {
               
                byte[] imageData = resultSet.getBytes("img");

             
                resultSet.close();
                statement.close();
                connection.close();

                return imageData;
            } else {
                JOptionPane.showMessageDialog(this, "No image found in the database.");
            }

           
            resultSet.close();
            statement.close();
            connection.close();
        } catch (SQLException e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Failed to retrieve image from the database.");
        }

        return null;
    }

   
    
    
